package main

import "fmt"

func main() {
	data := []byte{
		0x00, 0x00, 0x00, 0x00, 0x00, 0x60, 0xff, 0x03, // 000~007, 008~015, 016~023, 024~031, 032~039, 040~047, 048~055, 056~063
		0x00, 0x00, 0x00, 0x80, 0xfe, 0xff, 0xff, 0x07, // 064~071, 072~079, 080~087, 088~095, 096~103, 104~111, 112~119, 120~127
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // 128~135, 136~143, 144~151, 152~159, 160~167, 168~175, 176~183, 184~191
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // 192~199, 200~207, 208~215, 216~223, 224~231, 232~239, 240~247, 248~255
	}

	for i, datum := range data {
		for j := 0; j < 8; j++ {
			if datum&(1<<j) != 0 {
				fmt.Printf("%v", string(rune(i*8+j)))
			}
		}
	}
	fmt.Printf("\n")
}
